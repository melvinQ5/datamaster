, od_stat as (
select prod.BoxSKU 
	,year(SettlementTime) 结算年份
	,month(SettlementTime) 结算月份
    ,count( distinct concat(shopcode,SellerSku) ) 出单链接数
    ,round( sum( TotalGross/ExchangeUSD),2 )  扣税扣退款销售额
    ,count( distinct PlatOrderNumber ) 订单数
from wt_orderdetails wo
join prod on wo.BoxSku  = prod.boxsku 
left join tb on wo.shopcode = tb.arr
where  tb.arr is null and SettlementTime >= '2022-01-01'
group by prod.BoxSKU  , year(SettlementTime)  ,month(SettlementTime)
order by prod.BoxSKU  , year(SettlementTime)  ,month(SettlementTime)
)

, lst_stat as (
select wl.boxsku ,year(PublicationDate) year ,month(PublicationDate) month 
	,count(distinct concat(shopcode,SellerSku) ) 刊登链接数
from wt_listing wl 
join prod on wl.BoxSku  = prod.boxsku 
group by wl.BoxSku ,year(PublicationDate) ,month(PublicationDate)
)

,lst_prod_stat as (
select wl.boxsku ,min(MinPublicationDate) 产品首次刊登时间
from wt_listing wl 
join prod on wl.BoxSku  = prod.boxsku 
group by wl.boxsku 
)

,t0 as (
select boxsku ,year ,month 
from prod join 
( select distinct year ,month from dim_date dd 
where dd.full_date >='2022-01-01' and dd.full_date <'2023-10-01' ) dd 
order by boxsku ,year ,month 
)

,res as (
select t0.* 
	,date(产品首次刊登时间)
	,ifnull(刊登链接数,0) 刊登链接数 
	,ifnull(出单链接数,0) 出单链接数 
	,扣税扣退款销售额 ,订单数
from t0
left join od_stat t1 on t0.boxsku  = t1.boxsku and t0.year =t1.结算年份  and t0.month = t1.结算月份
left join lst_stat t2 on t0.boxsku  = t2.boxsku and t0.year =t2.year  and t0.month = t2.month 
left join lst_prod_stat t3 on t0.boxsku  = t3.boxsku 
)
